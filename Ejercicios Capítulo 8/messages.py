# Ejercicio 8-9
# Lista pequeña con mensajes aleatorios
mensajes = ['Hola mundo!', 'Esto es Python', 'Que agradable sujeto', 'Number 8']


# Función que imprime por consola los mensajes de una lista pasada como
# parámetro de entrada
def imprimir_mensajes(lista_mensajes):
    for mensaje in lista_mensajes:
        print(mensaje)


imprimir_mensajes(mensajes)
